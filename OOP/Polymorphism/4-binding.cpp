/*
***Overloading Function Concept***
Binding
-> process of connecting function call to its corresponding
    implementation
-> Two types: Early binding, Late Binding
->Used in DMA

Early Binding/Static Binding:
->Occus in compile time
->Compiler Links call to its corresponding function implementation
    based on its decalred type of variable or object


Late Binding/Dynamic Bidning:
->Occurs in runtime
-> When function call is made, the actual type of object being pointed to
    is used to link the call of corresponding function implementation
-> Used in Virtual Functions


*/